def color_red(input):
    return f"\033[31m{input}\033[0m"

def color_green(input):
    return f"\033[32m{input}\033[0m"

def color_yellow(input):
    return f"\033[33m{input}\033[0m"

def color_blue(input):
    return f"\033[34m{input}\033[0m"

def color_magenta(input):
    return f"\033[35m{input}\033[0m"

def color_cyan(input):
    return f"\033[36m{input}\033[0m"


